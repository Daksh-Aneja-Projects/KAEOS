import logging
from typing import Dict, Any

logger = logging.getLogger(__name__)

class OptionEvaluationEngine:
    """
    Tier 2 Deep Option Evaluation Engine.
    Executes heavy background simulations across 10 dimensions for generated options.
    """
    
    def __init__(self, enterprise_graph):
        self.graph = enterprise_graph

    async def evaluate_option_deep(self, option: Dict[str, Any], context: Dict[str, Any]) -> Dict[str, Any]:
        """
        Evaluates options on 8 strategic dimensions based on Enterprise State and Archetype.
        No domain-specific assumptions (HR, Finance) are permitted here.
        """
        logger.info(f"EvaluationEngine: Strategic Evaluation for: {option['action']}")
        
        ent_state = context.get("enterprise_state", {})
        budget_available = ent_state.get("budget", 0)
        goal_deadline_days = ent_state.get("goal_deadline_days", 90)
        enterprise_type = ent_state.get("type", "Standard")
        
        action = option.get("action", "")
        cost = option.get("estimated_cost", 0)
        recovery_days = option.get("recovery_days_estimate", 30)
        
        # 1. Universal Dimensions (1-100 scales)
        # Sourced dynamically from option schema generated by active department.
        dims = {
            "cost": option.get("dim_cost", 50),
            "time": option.get("dim_time", 50),
            "risk": option.get("dim_risk", 50),
            "stability": option.get("dim_stability", 50),
            "capability": option.get("dim_capability", 50),
            "reversibility": option.get("dim_reversibility", 50),
            "dependency": option.get("dim_dependency", 50),
            "confidence": option.get("dim_confidence", 50)
        }
        
        # Universal Enterprise Weights
        weights = {
            "cost": 0.15,          # lower is better (inverted below)
            "time": 0.10,          # lower is better (inverted below)
            "risk": 0.15,          # lower is better (inverted below)
            "stability": 0.15,
            "capability": 0.20,
            "reversibility": 0.05,
            "dependency": 0.10,    # lower is better (inverted below)
            "confidence": 0.10
        }
        
        # Shift weights based on Enterprise Type abstracted physics
        if enterprise_type == "Consulting":
            weights["capability"] = 0.30
            weights["stability"] = 0.05
            weights["cost"] = 0.00
        elif enterprise_type == "Manufacturing":
            weights["stability"] = 0.35
            weights["capability"] = 0.05
            weights["time"] = 0.20

        # Hard Constraints
        constraint_penalty = 0
        negatives = []
        positives = []
        
        if cost > budget_available:
            constraint_penalty += 40
            negatives.append(f"Option cost (${cost}) exceeds budget (${budget_available}).")
        else:
            positives.append("Option cost is within budget.")
            
        if recovery_days > goal_deadline_days:
            constraint_penalty += 40
            negatives.append(f"Recovery ({recovery_days} days) misses goal deadline ({goal_deadline_days} days).")
        else:
            positives.append("Recovery secures goal deadline.")

        # Calculate final strategic score
        raw_score = (
            ((100 - dims["cost"]) * weights["cost"]) +
            ((100 - dims["time"]) * weights["time"]) +
            ((100 - dims["risk"]) * weights["risk"]) +
            (dims["stability"] * weights["stability"]) +
            (dims["capability"] * weights["capability"]) +
            (dims["reversibility"] * weights["reversibility"]) +
            ((100 - dims["dependency"]) * weights["dependency"]) + 
            (dims["confidence"] * weights["confidence"])
        )
        
        # Build explaining strings based on dimensions
        if dims["capability"] > 70: positives.append("Strong capability restoration.")
        if dims["capability"] < 30: negatives.append("Insufficient capability impact.")
        
        if dims["dependency"] > 70: negatives.append("Increases systemic dependency risk.")
        if dims["stability"] < 40: negatives.append("High risk of destabilizing active operations.")

        final_score = max(0, min(100, raw_score - constraint_penalty))
        
        # Inject Learning Feedback
        learning_engine = context.get("learning_engine")
        learning_modifier = 0.0
        if learning_engine:
            domain = context.get("domain", "HR")
            enterprise_type_key = enterprise_type if enterprise_type != "Standard" else ent_state.get("type", "Standard")
            learning_modifier = learning_engine.get_historical_learning_score(action, domain=domain, enterprise_type=enterprise_type_key)
            final_score = max(0, min(100, final_score + learning_modifier))
        
        # The ultimate "why"
        why_ranked = f"Scored {round(final_score,1)} due to strong performance in "
        best_dim = max(dims, key=dims.get)
        why_ranked += f"{best_dim.replace('_', ' ')}."
        if constraint_penalty > 0:
            why_ranked += " Heavily penalized by hard constraints."
        if learning_modifier > 0:
            why_ranked += f" Boosted by historical success (+{round(learning_modifier,1)})."
        elif learning_modifier < 0:
            why_ranked += f" Penalized by historical failure ({round(learning_modifier,1)})."

        return {
            **option,
            "evaluation_tier": 2,
            "deep_decision_quality": round(final_score, 2),
            "dimensions": dims,
            "positives": positives,
            "negatives": negatives,
            "why_ranked_here": why_ranked
        }
